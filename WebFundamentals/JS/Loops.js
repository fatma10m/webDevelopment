// While Loops

console.log(0);
console.log(1);
console.log(2);
console.log(3);
console.log(4);
console.log(5);

var num = 0;    // start a variable at 0
while (num <= 5) {        
    console.log(num);    // print the current value of num        
    num=num+1;       // increment num by 1
}

// For Loops
var num = 0;
while(num <=5) {
    console.log(num);
    num = num + 1;
}

for (var num = 0; num <= 5; num = num +1) {    
    console.log(num);
}
