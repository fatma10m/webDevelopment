// 2 Ways to Decrement By Any Number
// these 2 statements are equivalent!
num = num - 2
num -= 2 

// 2 Ways to Increment By Any Number
// these 2 statements are equivalent!
num = num + 5
num += 5

// 3 Ways to Decrement By 1
// these 3 statements are equivalent!
num = num - 1
num -= 1
num--

// 3 Ways to Increment By 1
// these 3 statements are equivalent!
num = num + 1
num += 1
num++